package pattern;

public class Pattern16 {

	/**
	 * @param args
	 */
	public static void main(String[] args) 
	{
	  for(int i=0;i<6;i++)
	  {
		  for(int j=1;j<=i;j++)
		  {
			  System.out.print((i+j)%2);
		  }
		  System.out.println();
	  }
	  
	  for(int i=0;i<6;i++)
	  {
		  for(int j=1;j<=i;j++)
		  {
			  System.out.print(i%2);
		  }
		  System.out.println();
	  }
	  
	  
	  
	  
	  
	  
	}

}
